package main.java.Core.Wordament; 
public class Wordament {
    private String word;
    private String meaning;

    public Wordament(String word, String meaning) {
        this.word = word;
        this.meaning = meaning;
    }

    public String getWord() {
        return word;
    }

    public String getMeaning() {
        return meaning;
    }
}
